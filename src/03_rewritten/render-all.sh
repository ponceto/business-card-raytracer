#!/bin/sh

set -x

# ----------------------------------------------------------------------------
# configuration
# ----------------------------------------------------------------------------

SCENES="
aek
ponceto
smiley
simple
"

CARD_BIN="./card.bin"
CARD_W="960"
CARD_H="540"
SAMPLES="256"

# ----------------------------------------------------------------------------
# render all scenes
# ----------------------------------------------------------------------------

for SCENE in ${SCENES}
do
    ${CARD_BIN} --scene="${SCENE}" --width="${CARD_W}" --height="${CARD_H}" --samples="${SAMPLES}" --output="${SCENE}.ppm"
done

# ----------------------------------------------------------------------------
# End-Of-File
# ----------------------------------------------------------------------------
